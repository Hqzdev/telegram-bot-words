def test_credentials_file():
    """–¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–∞ —É—á–µ—Ç–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö"""
    print("\nüîç –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–∞ —É—á–µ—Ç–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö...")
    
    credentials_file = os.getenv('GOOGLE_CREDENTIALS_FILE', 'credentials.json')
    
    if os.path.exists(credentials_file):
        try:
            with open(credentials_file, 'r') as f:
                data = json.load(f)
            
            if 'client_email' in data and 'private_key' in data:
                print(f"‚úÖ –§–∞–π–ª {credentials_file} —Å–æ–¥–µ—Ä–∂–∏—Ç –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ")
                print(f"üìß Service Account email: {data['client_email']}")
                return True
            else:
                print(f"‚ùå –§–∞–π–ª {credentials_file} –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –ø–æ–ª—è")
                return False
                
        except json.JSONDecodeError:
            print(f"‚ùå –§–∞–π–ª {credentials_file} –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–º JSON")
            return False
        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ —á—Ç–µ–Ω–∏—è —Ñ–∞–π–ª–∞ {credentials_file}: {e}")
            return False
    else:
        print(f"‚ùå –§–∞–π–ª {credentials_file} –Ω–µ –Ω–∞–π–¥–µ–Ω")
        return False

